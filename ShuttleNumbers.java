public class ShuttleNumbers {
    public static void main(String[] args) {
        for (int i = -3; i < 9; i++) {
            for (int j = -3; j < 9; j++) {
                if (i != 4 && j != 4)
                    System.out.println(i + "номер шатла" + j);
            }
        }
    }
}

















